#include <stdio.h>

int main(void)
{
	int a,b;
	int ans = 0;
	scanf("%d %d", &a, &b);
	
	/*
	奇数の場合→1点問題は解けている→すぬけ+1
	4以上の場合→4点問題が解けている→すぬけ+4
	7,6,3,2の場合(a%4 == 3) || (a%4 == 2)→2点問題が解けている
	*/
	
	if( (a % 2 == 1) || (b % 2 == 1) ) ans+=1;
	if( (a >= 4) || (b >= 4) ) ans += 4;
	if( (a%4  == 3) || (a%4  == 2) || (b%4  == 3) || (b%4  == 2) ) ans += 2;
	
	printf("%d\n", ans);
	
	
	
	
	return 0;
}