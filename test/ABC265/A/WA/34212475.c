#include<stdio.h>
int main()
{
	long long n,m,t;
	scanf("%lld%lld%lld",&n,&m,&t);
	long long a[n-1],i,x[m],y[m],j;
	int check=0,flag=0;
	for(i=0;i<n-1;i++)
	{
		scanf("%lld",&a[i]);
	}
	for(j=0;j<m;j++)
	{
		scanf("%lld%lld",&x[j],&y[j]);
		if(j==0)
		{
			for(i=0;i<x[j]-1;i++)
			{
				t-=a[i];
				if(t<=0)
				{
					check=1;
					flag=1;
					break;
				}
			}
			t+=y[j];
		}
		else
		{
			for(i=x[j-1]-1;i<x[j]-1;i++)
			{
				t-=a[i];
				if(t<=0)
				{
					check=1;
					flag=1;
					break;
				}
			}
			t+=y[j];
		}
		if(flag==1)
		{
			break;
		}
	}
	if(check==1)
	{
		printf("No");
	}
	else
	{
		printf("Yes");
	}
}
